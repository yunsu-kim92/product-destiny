export function translate(key, language, values = {}) {
  const dict = translations[language] || translations[DEFAULT_LANGUAGE];
  const template =
    dict[key] ||
    translations.en?.[key] ||
    translations[DEFAULT_LANGUAGE][key] ||
    key;

  return template.replace(/\{(\w+)\}/g, (_, token) => values[token] ?? '');
}
